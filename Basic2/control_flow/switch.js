const month = "April";

switch (month) {
    case "January":
        console.log("Jan");
        break;

    case "February":
        console.log("Feb");
        break;

    case "March":
        console.log("March");
        break;

    case "April":
        console.log("April");
        break;

    case "May":
        console.log("May");
        break;

    default:
        console.log("default case match");
        break;
}